CREATE CLASS OfflineToken;
CREATE PROPERTY OfflineToken.id STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.subject STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.username STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.description STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.bearerToken STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.refreshToken STRING (NOTNULL, MANDATORY TRUE);
CREATE PROPERTY OfflineToken.created DATETIME (NOTNULL, MANDATORY TRUE, DEFAULT 'sysdate()');
ALTER CLASS OfflineToken STRICTMODE TRUE;
CREATE INDEX OfflineToken_id_idx ON OfflineToken (id) UNIQUE;
CREATE INDEX OfflineToken_bearer_idx ON OfflineToken (bearerToken) UNIQUE;
CREATE INDEX OfflineToken_subject_idx ON OfflineToken (subject) NOTUNIQUE;
