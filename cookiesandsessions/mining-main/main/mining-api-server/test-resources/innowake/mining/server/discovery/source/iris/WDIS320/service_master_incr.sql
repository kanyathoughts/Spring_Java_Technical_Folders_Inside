ALTER SESSION SET CURSOR_SHARING=EXACT;
SET VERIFY OFF
SET TERMOUT OFF
SET NEWPAGE 0
SET SPACE 0
SET LINESIZE 78
SET PAGESIZE 0
SET ECHO OFF
SET FEEDBACK OFF
SET HEADING OFF
SPOOL BEN_WORK:SERVICE_MASTER_INCR.TXT
SELECT SERVICE SVC
     , STATUS U
     , MNEMONIC MN
     , DESCRIPTION
     , EXTERNAL_SERVICE XSV
     , EOB_DESCRIPTION EOB
     , SUBSTR(TO_CHAR(DISCOUNT_PERCENT,'99'),-2) DP
     , REPORT_CATEGORY R
     , SHARED_RISK_CATEGORY S
     , HOSPITAL H
     , CHECK_OCCURRENCES O
     , MDR_PROCEDURE M
     , REQUEST_OCCURRENCES Q
     , REQUEST_POT P
     , REQUEST_PROCEDURE_CODE R
     , REQUEST_ANESTHESIA_UNITS A
     , NO_ENTRY N
     , BENEFIT_RULE_SERVICE B
  FROM BRC230_SERVICE_MASTER
 WHERE UPDATE_DATE > '&1'
ORDER BY 1;
SPOOL OFF
EXIT
